package sew.maps.tomtom.util;

public class GridPosition {

	private final int x;
	private final int y;

	public GridPosition(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

	/**
	 * @return the x
	 */
	public int getX() {
		return this.x;
	}

	/**
	 * @return the y
	 */
	public int getY() {
		return this.y;
	}

}
